package plugins;

import com.sun.squawk.VM;
import sics.plugin.PlugInComponent;

public class CL extends PlugInComponent {
	public CL() {

	}

	public CL(String[] args) {
		super(args);
	}

	public void printString() {
		while (true) {
			VM.println("Hello World from CL (only bytes)!\r\n");
			VM.println("Is it really working dynamically?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????\r\n");
			VM.println("Oh, yes, it is\r\n");
			VM.println("Jakob is here!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!\r\n");
			VM.println("Eilert is back!\r\n");
			VM.println("new try...");
			VM.println("Now it should work!!\r\n");
			try {
				Thread.sleep(2000);
			} catch (InterruptedException e) {
				VM.println("Interrupted.\r\n");
			}
		}
	}

	public static void main(String[] args) {
		VM.println("CL.main()\r\n");
		CL cli = new CL();
		cli.printString();
		VM.println("CLI-main done\r\n");
	}

	public void init() {
		System.out.println("Init ...");
	}

	public void run() {
		System.out.println("Hello world");
	}
}
